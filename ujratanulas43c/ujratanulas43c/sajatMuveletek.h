#ifndef SAJATMUVELETEK_H
#define SAJATMUVELETEK_H

namespace sajatMuveletek
{
	double osszeadas(double szam1, double szam2)
	{
		return szam1 + szam2;
	}

	double kivonas(double szam1, double szam2)
	{
		return szam1 - szam2;
	}

	double szorzas(double szam1, double szam2)
	{
		return szam1 * szam2;
	}

	double osztas(double szam1, double szam2)
	{
		return szam1 / szam2;
	}

}

#endif


